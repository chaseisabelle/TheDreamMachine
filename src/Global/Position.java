package Global;

/**
 * This class represents a percise position.
 *
 * @author chase isabelle
 */
public class Position {
    
    /**
     * Default constructor
     * 
     * @param x is the x position
     * @param y is the y position
     */
    public Position(double x, double y) {
        this.x = x;
        this.y = y;
    }
    
    /**
     * Position attributes
     */
    public double x;
    public double y;

}
